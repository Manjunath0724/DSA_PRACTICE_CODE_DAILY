package Chapter8;

public class switching {
    public static void main(String[] args) {
        int number = 2;
        switch (number) {
            case 1:
                System.out.println("Samosa");
                break;
            case 2:
                System.out.println("Vadapav");
                break;
            case 3:
                System.out.println("pani puri");
                break;

            default:
                System.out.println("We wake up");

        }
    }
}
